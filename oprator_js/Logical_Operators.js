let q = 5;
let z = 10;

// Logical AND (&&) operator
if (q > 5 && z > 7) {
    console.log("Both q and z are positive");
} else {
    console.log("both q and z are nagative")
}

// Logical OR (||) operator
if (q > 0 || z > 0) {
    console.log("At least one of q or z is positive");
}

// Logical NOT (!) operator
if (!(q > 0)) {
    console.log("q is not positive");
}

